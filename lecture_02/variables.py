# a variable is a memory location to store value(s)
# 1. variable naming rules (cannot break)
# i) can only contain alphabets, _ and numbers
# ii) must start with alphabets or _
# iii) cannot be keywords
# eg. valid names are:
# a
# _theskyisblue
# num_people
#
# eg. invalid names are:
# 4ever     -> cannot start with number
# this+that  -> cannot contain +
# class      -> is a keyword

# 2. good variable names (guideline)
# i) must be meaningful
# ii) use all lower case for variables and function names
# iii) use _ to connect words
# eg. good names
# age
# student_count
# is_valid_number
#
# eg. bad names
# a   # what does it mean?
# s_count    # what s mean?
# function_of_what_a_valid_number_is
# _full_name   # don't start with _

# types of variables
# 1. string
# 2. int
# 3. float
# 4. bool

